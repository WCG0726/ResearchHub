          <p><strong>学位论文：</strong>[序号] 作者. 题名[D]. 学校所在地: 学校名, 年.</p>
          <p><strong>会议论文：</strong>[序号] 作者. 题名[C]//会议名. 地点: 出版者, 年: 页码.</p>
        </div>
        <p class="tip-title">作者姓名格式</p>
        <ul class="tip-list">
